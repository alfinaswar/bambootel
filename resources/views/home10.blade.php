    <script>
        document.addEventListener('DOMContentLoaded', function () {

            // ======================= 1. LOGIKA HEADER & MENU MOBILE =======================
            // (Kode ini sama seperti sebelumnya, untuk sticky header & menu mobile)
            const header = document.getElementById('main-header');
            if (header) {
                window.addEventListener('scroll', () => {
                    if (window.scrollY > 50) header.classList.add('scrolled');
                    else header.classList.remove('scrolled');
                });
            }

            // ======================= 2. LOGIKA ANIMASI GERBANG TERBUKA =======================
            const gateSection = document.getElementById('gate');

            const gateObserver = new IntersectionObserver((entries) => {
                entries.forEach(entry => {
                    // Saat bagian bawah gerbang terlihat saat scroll ke atas
                    // atau saat bagian atas gerbang tidak terlihat saat scroll ke bawah
                    if (window.scrollY > entry.target.offsetTop) {
                        document.body.classList.add('gate-opened');
                    } else {
                        document.body.classList.remove('gate-opened');
                    }
                });
            }, { threshold: [0, 1] }); // Amati saat masuk dan keluar viewport

            if (gateSection) {
                gateObserver.observe(gateSection);
            }


            // ======================= 3. LOGIKA ANIMASI UMUM SAAT SCROLL =======================
            const sectionsToAnimate = document.querySelectorAll('.animate-on-scroll');

            const contentObserver = new IntersectionObserver((entries, observer) => {
                entries.forEach(entry => {
                    if (entry.isIntersecting) {
                        entry.target.classList.add('is-visible');
                        observer.unobserve(entry.target); // Hentikan pengamatan setelah animasi
                    }
                });
            }, { threshold: 0.1 }); // Animasi terpicu saat 10% elemen terlihat

            sectionsToAnimate.forEach(section => {
                contentObserver.observe(section);
            });


            // ======================= 4. LOGIKA CAROUSEL VILA =======================
            // (Kode ini sama seperti sebelumnya, untuk fungsionalitas carousel)
            const slider = document.querySelector('.villa-slider');
            const prevBtn = document.getElementById('prev-btn');
            const nextBtn = document.getElementById('next-btn');

            if (slider && prevBtn && nextBtn) {
                const scrollStep = document.querySelector('.villa-card').offsetWidth + 30;

                nextBtn.addEventListener('click', () => {
                    slider.parentElement.scrollBy({ left: scrollStep, behavior: 'smooth' });
                });
                prevBtn.addEventListener('click', () => {
                    slider.parentElement.scrollBy({ left: -scrollStep, behavior: 'smooth' });
                });
            }
        });
    </script>
